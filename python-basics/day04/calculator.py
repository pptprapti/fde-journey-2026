num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

addition = num1 + num2
print(f"{num1} + {num2} = {addition}")

sub = num1 - num2
print(f"{num1} - {num2} = {sub}")

multi = num1 * num2
print(f"{num1} * {num2} = {multi}")

div = num1 / num2
print(f"{num1} / {num2} = {div}")